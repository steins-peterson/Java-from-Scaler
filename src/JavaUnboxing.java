public class JavaUnboxing {
    public static void main(String[] args) {
        Integer a = 10;
        Double b= 10.7;
        Character c = 'J';

        int d = a;
        double e = b;
        char f  = c;

        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
    }
}
